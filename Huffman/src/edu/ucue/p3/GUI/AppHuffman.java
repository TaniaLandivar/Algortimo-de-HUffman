    private void generarTabla(String[][] listaTablas) throws FileNotFoundException, IOException {
        DefaultTableModel modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        Object[] tabla = new Object[4];
        tabla[0] = "Caracter";
        tabla[1] = "Frecuencia";
        tabla[2] = "Bits Normal";
        tabla[3] = "Bits Huffman";
        
        modelo.setColumnIdentifiers(tabla);
        
        if(listaTablas==null || listaTablas.length == 0){
            this.tSalida.setModel(modelo);
            return;
        }
        
        for (int i=0; i<listaTablas.length; i++) {
            modelo.addRow(listaTablas[i]);
        }
        this.tSalida.setModel(modelo);
    }
    
