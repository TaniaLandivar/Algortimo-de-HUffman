    private void bComprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bComprimirActionPerformed
        try {
            // TODO add your handling code here:
            Huffman huffman = new Huffman(archivo);
            //System.out.println(huffman.gethC().getTexto().toString());
            bComprimir.setEnabled(false);
            generarTabla(huffman.getTabla());
            LbRazonCompresion.setText(String.valueOf(huffman.razonDeCompresion()));
            File original = new File("original.txt");
            File comprimido = new File("comprimido");
            LBOriginal.setText(String.valueOf(original.length()));
            LbComprimido.setText(String.valueOf(comprimido.length()));
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Archivo no disponible");
        }
        
    }//GEN-LAST:event_bComprimirActionPerformed

